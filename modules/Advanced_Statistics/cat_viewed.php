if ( !defined('XCART_SESSION_START') ) { header("Location: ../../"); die("Access denied"); }

if (empty($active_modules['Advanced_Statistics']) || $config['Advanced_Statistics']['enable_shop_statistics'] != 'Y')
    return;

if (!empty($cat)) {
    $use_delayed_update = ($config['Advanced_Statistics']['use_delayed_stats_update'] == 'Y')
        ? 'views_stats_categories'
        : false;

    db_query("UPDATE $sql_tbl[categories] SET views_stats = (views_stats + 1) WHERE categoryid = '$cat'", $use_delayed_update);
    db_query("INSERT INTO $sql_tbl[stats_shop](id,action,date) VALUES('$cat','C','".XC_TIME."')");
}
